export const getStoredReviews = () => {
  const stored = localStorage.getItem('itsablog_reviews');
  return stored ? JSON.parse(stored) : mockReviews;
};

export const saveReview = (review) => {
  const reviews = getStoredReviews();
  reviews.unshift(review);
  localStorage.setItem('itsablog_reviews', JSON.stringify(reviews));
};

export const getStoredLists = () => {
  const stored = localStorage.getItem('itsablog_lists');
  return stored ? JSON.parse(stored) : [];
};

export const saveList = (list) => {
  const lists = getStoredLists();
  lists.push(list);
  localStorage.setItem('itsablog_lists', JSON.stringify(lists));
};

export const addBookToList = (listId, bookId) => {
  const lists = getStoredLists();
  const list = lists.find(l => l.id === listId);
  if (list && !list.books.includes(bookId)) {
    list.books.push(bookId);
    localStorage.setItem('itsablog_lists', JSON.stringify(lists));
  }
};
